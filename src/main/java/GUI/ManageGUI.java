    private void btnisertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnisertActionPerformed
        // TODO add your handling code here:
        String Firstname = txtfirstname.getText();
        String Lastname = txtlastname.getText();
        int age = Integer.parseInt(txtage.getText());
        int grade = Integer.parseInt(cmdgrade.getSelectedItem().toString());
        String Gender;
        if (rdomale.isSelected()) {
            Gender = "Male";
        } else if (rdofemale.isSelected()) {
            Gender = "Female";
        } else {
            Gender = "";
        }
        student = new Student(Firstname, Lastname, Gender, age, grade);
        boolean result = stDB.insert(student);
        if (result) {
            JOptionPane.showMessageDialog(null, "Adding successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Adding unsuccessfully");
        }
    }//GEN-LAST:event_btnisertActionPerformed

    private void btnviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewActionPerformed
        // TODO add your handling code here:
        // Clear existing rows first to prevent duplication
        tablemodel.setRowCount(0);
        studentlist = stDB.view();
        for (Student student : studentlist) {
            String firstname = student.getFirstName();
            String lastname = student.getLastName();
            String gender = student.getGender();
            int age = student.getAge();
            int grade = student.getGrade();
            tablemodel.addRow(new Object[]{firstname, lastname, age, gender, grade});
        }
    }//GEN-LAST:event_btnviewActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        int grade=Integer.valueOf(cmdgrade.getSelectedItem().toString());
        studentlist=stDB.search(grade);
        tablemodel.setRowCount(0);
        for (Student student : studentlist) {
            String firstname = student.getFirstName();
            String lastname = student.getLastName();
            String gender = student.getGender();
            int age = student.getAge();
            int grade1 = student.getGrade();
            tablemodel.addRow(new Object[]{firstname, lastname, age, gender, grade1});
        }
    }//GEN-LAST:event_btnsearchActionPerformed

    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) table1.getModel();
        int selectedRow=table1.getSelectedRow();
        txtfirstname.setText(model.getValueAt(selectedRow, 0).toString());
        txtlastname.setText(model.getValueAt(selectedRow, 1).toString());
        txtage.setText(model.getValueAt(selectedRow, 2).toString());
        if(model.getValueAt(selectedRow, 3).toString().equals("Male")){
            rdomale.setSelected(true);
        }
        else{
            rdofemale.setSelected(true);
        }
        cmdgrade.setSelectedItem(model.getValueAt(selectedRow, 4).toString());
        
    }//GEN-LAST:event_table1MouseClicked

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
         // Get firstname from text field (NOT the selected text)
    String firstname = txtfirstname.getText();

    // Delete from file
    boolean confirm = stDB.delete(firstname);
         
    if(confirm){
        JOptionPane.showMessageDialog(null, "Deleted successfully");
        
        // Refresh table view after deletion
        tablemodel.setRowCount(0);  
        studentlist = stDB.view();
        for (Student student : studentlist) {
            tablemodel.addRow(new Object[]{
                student.getFirstName(),
                student.getLastName(),
                student.getAge(),
                student.getGender(),
                student.getGrade()
            });
        }

        // Clear input fields
        txtfirstname.setText("");
        txtlastname.setText("");
        txtage.setText("");
        buttonGroup1.clearSelection();
    }
    else{
        JOptionPane.showMessageDialog(null, "Delete failed");
    }
    }//GEN-LAST:event_btndeleteActionPerformed

